package com.omototest.jsonobjects;

public class OutgoingCampaignsJson {
	private Long id;
	private String type;
	private String campaignjson;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCampaignjson() {
		return campaignjson;
	}
	public void setCampaignjson(String campaignjson) {
		this.campaignjson = campaignjson;
	}
}
